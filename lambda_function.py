def get_welcome_response():
    """ If we wanted to initialize the session to have some attributes we could
    add those here
    """

    session_attributes = {}
    card_title = "Welcome"
    speech_output = "Welcome to FRED. " \
                    "FRED provides information about economic data series. " \
                    "For example, you can ask what is the most recent \
                     value of GDP. "
    # If the user either does not reply to the welcome message or says something
    # that is not understood, they will be prompted again with this text.
    reprompt_text = "Please tell me what economic data series you are interested in. " \
                    "You can say GDP or Gross Domestic Product. "
    should_end_session = False
    return build_response(session_attributes, build_speechlet_response(
        card_title, speech_output, reprompt_text, should_end_session))

def get_economic_series(intent,session):
    reprompt_text = None
    session_attributes = {}
    if 'Series' in intent['slots']:
        series = intent['slots']['Series']['value']
        if series.lower() in ['gdp','gross domestic product']:
            res = fr.series.observations('GDP',params=params)
            res = res[0]['value']

            val = inf.number_to_words(int(res*1000000000))
            #val = "12000"
        speech_output = "GDP is " + val + \
                        "."
        should_end_session = True
    else:
        speech_output = "I could not find the economic data series. " \
                        "Try again by asking for the value of GDP."
        should_end_session = False

    # Setting reprompt_text to None signifies that we do not want to reprompt
    # the user. If the user does not respond or says something that is not
    # understood, the session will end.
    return build_response(session_attributes, build_speechlet_response(
        intent['name'], speech_output, reprompt_text, should_end_session))

# --------------- Helpers that build all of the responses ----------------------


def build_speechlet_response(title, output, reprompt_text, should_end_session):
    return {
        'outputSpeech': {
            'type': 'PlainText',
            'text': output
        },
        'card': {
            'type': 'Simple',
            'title': 'SessionSpeechlet - ' + title,
            'content': 'SessionSpeechlet - ' + output
        },
        'reprompt': {
            'outputSpeech': {
                'type': 'PlainText',
                'text': reprompt_text
            }
        },
        'shouldEndSession': should_end_session
    }


def build_response(session_attributes, speechlet_response):
    return {
        'version': '1.0',
        'sessionAttributes': session_attributes,
        'response': speechlet_response
    }
